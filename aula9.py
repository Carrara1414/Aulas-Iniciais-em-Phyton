#operadores matemáticos
soma = 1 + 1 
print(soma)

subtracao = 1 - 1.90 #fiz subtração com numero quebrado
print(subtracao)

multiplicacao = 2 * 4.5
print(multiplicacao)

divisao = 10 / 2.59 #divisão, perceba que aqui, não exite arredondamento de numero, contamos o inteiro e os quebrados.
print(divisao)

divisao_inteira = 10 // 2.59 #divisão, onde só contamos os inteiros, e ignoramos o que vem depois da virgula
print(divisao_inteira)

potenciacao = 1000 ** 2
print(potenciacao)

modulo = 50 % 3 # resto de uma divisão 
print(modulo)

